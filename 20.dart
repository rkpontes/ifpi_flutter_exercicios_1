// Declare uma const chamada taxaDeConversao e use-a para converter um valor de dólares
// para reais, considerando que 1 dólar vale 5.23 reais. Imprima o resultado.

void main() {
  const double taxaDeConversao = 5.23;
  double valorEmDolares = 10;
  double valorEmReais = valorEmDolares * taxaDeConversao;

  print(valorEmReais);
}
