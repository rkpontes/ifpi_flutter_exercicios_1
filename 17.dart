// Crie uma variável final chamada diasDaSemana e atribua a ela uma lista com os dias da
// semana. Use um loop para imprimir todos os dias da lista.

void main() {
  final diasDaSemana = [
    "Segunda",
    "Terça",
    "Quarta",
    "Quinta",
    "Sexta",
    "Sábado",
    "Domingo"
  ];

  for (var dia in diasDaSemana) {
    print(dia);
  }
}
